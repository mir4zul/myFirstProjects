# Integer number
IntNum = 10

# floating point number
FloNum = 10.5

# Negative Number
NegNum = -20

# complex number
ComNum = 3 + 4i

# Big number
BigNum = 1234567890123456789012345678901234567890

# Scientific notation
SciNum = 1.5e3  # 1.5 * 10^3 = 1500.0

# Underscore in number for readability
# (available in Ruby 2.0 and later)
ReadableNum = 1_000_000


# Output the numbers
puts "Integer Number: #{IntNum}"
puts "Floating Point Number: #{FloNum}"
puts "Negative Number: #{NegNum}"
puts "Complex Number: #{ComNum}"
puts "Big Number: #{BigNum}"
puts "Scientific Notation: #{SciNum}"
puts "Readable Number: #{ReadableNum}"
puts "Type of IntNum: #{IntNum.class}"
puts "Type of FloNum: #{FloNum.class}"
puts "Type of NegNum: #{NegNum.class}"
puts "Type of ComNum: #{ComNum.class}"
puts "Type of BigNum: #{BigNum.class}"
puts "Type of SciNum: #{SciNum.class}"
puts "Type of ReadableNum: #{ReadableNum.class}"